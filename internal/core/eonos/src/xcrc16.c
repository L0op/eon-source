uint16_t xcrc16_update(uint16_t crc, uint8_t a) {
  crc ^= a;
  for (uint8_t i = 0; i < 8; ++i) {
    if (crc & 1)
      crc = (crc >> 1) ^ 0xA001;
    else
      crc = (crc >> 1);
  }
  return crc;
}